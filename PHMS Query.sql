use PHMS;

CREATE TABLE admins (
    id VARCHAR(9) PRIMARY KEY,
    name VARCHAR(MAX),
    age INT,
    phone_number VARCHAR(11),
    password VARCHAR(MAX),
    email VARCHAR(MAX)
);

CREATE TABLE pharmacist (
    ID VARCHAR(9) PRIMARY KEY,
    profile VARCHAR(MAX),
    password VARCHAR(MAX),
    email VARCHAR(MAX)
);

CREATE TABLE customer (
    id VARCHAR(9) PRIMARY KEY,
    password VARCHAR(MAX),
    profile VARCHAR(MAX),
    government VARCHAR(MAX),
    street_number INT,
    department_number INT,
    admin_id VARCHAR(9) REFERENCES admins,
    pharmacist_id VARCHAR(9) REFERENCES pharmacist,
    email VARCHAR(MAX),
    orders_count INT DEFAULT 0,
    review_out_of_5 FLOAT DEFAULT NULL,
    value_paid DECIMAL(10, 2) DEFAULT 0.00
);




create table mainseller(
quantity int, 
medicine_id int,
medicine_name varchar(255) primary key,
price int,
category varchar(max),
delivery_date date
);

 create table delivery(
addres varchar(255) primary key ,
delivery_time time
);

 create table medicine(
 medicine_id int primary key,
 medicine_name varchar(max),
 quantity int,
 dose int,
 prescrption varchar(max),
 category varchar(max),
 price int,
 expiration_date date,
 address_delivery varchar (255) foreign key references delivery
 );

 create table discount(
medicine_id int primary key,
receipt_value int,
discount_percentage int
);

 create table payment(
 custmer_id varchar(9) primary key,
 receipt_value int,
 payment_method varchar (max),
 medicine_id int foreign key references discount
 );

create table discountcode(
promocode varchar (255) primary key,
medicine_id int foreign key references discount
);

create table sales(
monthly_sold varchar(20) primary key,
high_demand_medicine varchar(255)
);

create table review(
customer_id varchar(9) primary key,
medicine_id int,
review_out_of_5 varchar(5)
);

create table sell(
medicine_id int foreign key references medicine,
medicine_name varchar (255) foreign key references mainseller
);

create table buy(
medicine_id int foreign key references medicine,
customer_id varchar(9) foreign key references customer
);

create table relatedtosales(
monthly_sold varchar (20) foreign key references sales,
medicine_id int foreign key references medicine
);

create table made(
customer_id varchar(9) foreign key references customer,
pcustomer_id varchar(9) foreign key references payment,
);

create table discountsales(
monthly_sold varchar (20) foreign key references sales,
medicine_id int foreign key references discount,
);

create table doneon(
customer_id varchar (9) foreign key references payment,
medicine_id int foreign key references medicine,
);

create table aboutreview(
customer_id varchar(9) foreign key references review,
medicine_id int foreign key references medicine,
);


